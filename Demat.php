<h2>What is a demat account?</h2>
<p> A demat account helps investors hold shares and securities in an electronic format. This kind of account is also called a dematerialised
account. It also helps to keep proper track of all the investments an individual makes in shares, exchange-traded funds, bonds, and
mutual funds in one place. <br> <br>
<h3> Importance of a demat account </h3><br>
1. Digitally secure way of holding shares and securities <br>
2. Eliminates theft, forgery, loss, and damage to the physical certificates <br>
3. Quick transfer of shares <br>
What is Dematerialisation (demat)? <br>
Dematerialisation is the process of converting the physical share certificates into electronic form, making it easy to maintain and access
them from anywhere. Earlier, shares were held in the form of physical certificates, which were cumbersome to store and transfer. The
advent of a demat account made it a lot easier to handle shares. An investor who wants to convert his physical shares into digital form
needs to open a demat account with a depository participant (DP).
</p>
<br>
<br>
<br>
<h2>Benefits of a demat account</h2>
<p> 
• Eliminates the risk of paper-based share certificates: Before the advent of the demat account, shares existed as physical paper
certificates. If you held shares of a company, you had to safe-keep a dozen of paper certificates. These were vulnerable to
tampering, theft, loss, and forgery. Moreover, the transfer of shares involved lengthy paperwork that was prone to error and delays.
With a demat account, you can store all these shares electronically in a safe and secure digital repository. <br>
• Convenient share storage and transfer: This allows you to store any number of shares, so you can trade in volumes and monitor the
details of all the shares you hold in a demat account. It further facilitates the swift transfer of shares while trading online. <br>
• Bonus stock-splits get automatically updated: If an initiative of the company whose shares you are holding brings in a change to its
stocks such as bonus issue, stock split, etc., it will be updated automatically in your demat account. <br>
• Stores various investments: Besides the shares, the demat account can also hold multiple assets, like bonds, mutual funds,
exchange traded funds, government securities, etc. <br>
• Easy access online: You can access your demat account anytime, anywhere, through your smartphone or laptop <br>
• Nomination: A demat account also provides the nomination facility as per the process described by the depository. In case of the
investor's demise, the appointed nominee will receive the shareholding in the demat account</p>
